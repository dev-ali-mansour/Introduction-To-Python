from decimal import Decimal

# Integer
print(int(123456789123456789))

# Float
print(float(123456789123456789))

# Decimals
print(Decimal('123456789123456789'))

# Printing floats
print(0.00001)
print(f"{0.00001:f}")
print(f"{0.0000001:f}")
print(f"{0.0000001:.7f}")

# Python division types
print(4 / 2)
print(4 // 2)  # Floored result
print(7 // 3)
